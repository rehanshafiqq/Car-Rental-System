CREATE TABLE Customers (
    CustomerID INT PRIMARY KEY,
    FirstName VARCHAR(50),
    LastName VARCHAR(50),
    Email VARCHAR(100),
    PhoneNumber VARCHAR(15),
    Address VARCHAR(255)
);
CREATE TABLE Cars (
    CarID INT PRIMARY KEY,
    Make VARCHAR(50),
    Model VARCHAR(50),
    Year INT,
    Color VARCHAR(50),
    RentalRatePerDay DECIMAL(10, 2),
    AvailabilityStatus VARCHAR(50)
);
CREATE TABLE RentalRecords (
    RentalID INT PRIMARY KEY,
    CustomerID INT,
    CarID INT,
    RentalStartDate DATE,
    RentalEndDate DATE,
    TotalRentalCost DECIMAL(10, 2),
    FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID),
    FOREIGN KEY (CarID) REFERENCES Cars(CarID)
);
CREATE TABLE Reservations (
    ReservationID INT PRIMARY KEY,
    CustomerID INT,
    CarID INT,
    ReservationDate DATE,
    ReservationStartDate DATE,
    ReservationEndDate DATE,
    FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID),
    FOREIGN KEY (CarID) REFERENCES Cars(CarID)
);
CREATE TABLE Payments (
    PaymentID INT PRIMARY KEY,
    RentalID INT,
    AmountPaid DECIMAL(10, 2),
    PaymentDate DATE,
    PaymentMethod VARCHAR(50),
    FOREIGN KEY (RentalID) REFERENCES RentalRecords(RentalID)
);

CREATE TABLE CarTypes (
    TypeID INT PRIMARY KEY,
    TypeName VARCHAR(50),
    Description TEXT,
    DailyRate DECIMAL(10, 2)
);
CREATE TABLE Locations (
    LocationID INT PRIMARY KEY,
    LocationName VARCHAR(100),
    Address VARCHAR(255),
    ContactPerson VARCHAR(100),
    PhoneNumber VARCHAR(15)
);
CREATE TABLE InsurancePlans (
    InsuranceID INT PRIMARY KEY,
    PlanName VARCHAR(100),
    CoverageDetails TEXT,
    PremiumRate DECIMAL(10, 2)
);
CREATE TABLE EmployeeRoles (
    RoleID INT PRIMARY KEY,
    RoleName VARCHAR(100),
    Responsibilities TEXT,
    Salary DECIMAL(10, 2)
);
CREATE TABLE Employees (
    EmployeeID INT PRIMARY KEY,
    RoleID INT,
    FirstName VARCHAR(50),
    LastName VARCHAR(50),
    Email VARCHAR(100),
    PhoneNumber VARCHAR(15),
    FOREIGN KEY (RoleID) REFERENCES EmployeeRoles(RoleID)
);


CREATE TABLE Invoices (
    InvoiceID INT PRIMARY KEY,
    RentalID INT,
    CustomerID INT,
    TotalAmount DECIMAL(10, 2),
    InvoiceDate DATE,
    DueDate DATE,
  
  FOREIGN KEY (RentalID) REFERENCES RentalRecords(RentalID),
    FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID)
);
